% pure tones

freqs = [33 34 36 39 41 44 46 49 52 55 58 62 ...
		 65 69 73 78 82 87 92 98 104 110 117 123 ...
		 131 139 147 156 165 175 185 196 208 220 233 247 ...
		 262 277 294 311 330 349 370 392 415 440 466 494 ...
		 523 554 587 622 659 698 740 784 831 880 932 988 1047 1108 1175 1245 1319 1397 1480 1568 1661 1760 1865 1976 2093 ...
		 2218 2349 2489 2637 2793 2960 3136 3322 3520 3729 3951 4186 4435 4599 4978 5274 5587 5919 6271 6644];
duration = 2;

for i = 1:4:48
	sampleFreq = 44100;
	dt = 1/sampleFreq;

	t = [0:dt:duration];

	% major chords
	s = sin(2*pi*freqs(i)*t) + sin(2*pi*freqs(i+4)*t) + sin(2*pi*freqs(i+7)*t) + ...
		sin(2*pi*freqs(i+12)*t) + sin(2*pi*freqs(i+12+4)*t) + sin(2*pi*freqs(i+12+7)*t) + ...
		sin(2*pi*freqs(i+12+12)*t) + sin(2*pi*freqs(i+12+12+4)*t) + sin(2*pi*freqs(i+12+12+7)*t) + ...
		sin(2*pi*freqs(i+12+12+12)*t) + sin(2*pi*freqs(i+12+12+12+4)*t) + sin(2*pi*freqs(i+12+12+12+7)*t);
	%sound(s,sampleFreq);
	wavName = sprintf('chords/tone%d.wav',i);
	wavwrite(s,sampleFreq,16,wavName);

	% minor chords
	s = sin(2*pi*freqs(i)*t) + sin(2*pi*freqs(i+3)*t) + sin(2*pi*freqs(i+7)*t) + ...
		sin(2*pi*freqs(i+12)*t) + sin(2*pi*freqs(i+12+3)*t) + sin(2*pi*freqs(i+12+7)*t) + ...
		sin(2*pi*freqs(i+12+12)*t) + sin(2*pi*freqs(i+12+12+3)*t) + sin(2*pi*freqs(i+12+12+7)*t) + ...
		sin(2*pi*freqs(i+12+12+12)*t) + sin(2*pi*freqs(i+12+12+12+3)*t) + sin(2*pi*freqs(i+12+12+12+7)*t);
	%sound(s,sampleFreq);
	wavName = sprintf('chords/tone%d.wav',i+1);
	wavwrite(s,sampleFreq,16,wavName);

	% aum chords
	s = sin(2*pi*freqs(i)*t) + sin(2*pi*freqs(i+4)*t) + sin(2*pi*freqs(i+8)*t) + ...
		sin(2*pi*freqs(i+12)*t) + sin(2*pi*freqs(i+12+4)*t) + sin(2*pi*freqs(i+12+8)*t) + ...
		sin(2*pi*freqs(i+12+12)*t) + sin(2*pi*freqs(i+12+12+4)*t) + sin(2*pi*freqs(i+12+12+8)*t) + ...
		sin(2*pi*freqs(i+12+12+12)*t) + sin(2*pi*freqs(i+12+12+12+4)*t) + sin(2*pi*freqs(i+12+12+12+8)*t);
	%sound(s,sampleFreq);
	wavName = sprintf('chords/tone%d.wav',i+2);
	wavwrite(s,sampleFreq,16,wavName);

	% dim chords
	s = sin(2*pi*freqs(i)*t) + sin(2*pi*freqs(i+3)*t) + sin(2*pi*freqs(i+6)*t) + ...
		sin(2*pi*freqs(i+12)*t) + sin(2*pi*freqs(i+12+3)*t) + sin(2*pi*freqs(i+12+6)*t) + ...
		sin(2*pi*freqs(i+12+12)*t) + sin(2*pi*freqs(i+12+12+3)*t) + sin(2*pi*freqs(i+12+12+6)*t) + ...
		sin(2*pi*freqs(i+12+12+12)*t) + sin(2*pi*freqs(i+12+12+12+3)*t) + sin(2*pi*freqs(i+12+12+12+6)*t);
	%sound(s,sampleFreq);
	wavName = sprintf('chords/tone%d.wav',i+3);
	wavwrite(s,sampleFreq,16,wavName);
end